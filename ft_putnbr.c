#include "so_long.h"

void	ft_putnbr(int nbr)
{
	if (nbr <= 9)
	{
		nbr += '0';
		write(1, &nbr, 1);
	}
	else
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
}
