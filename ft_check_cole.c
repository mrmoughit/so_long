#include "so_long.h"

int	ft_find_col(char **s)
{
	int	i;
	int	j;

	i = 0;
	while (s[i])
	{
		j = 0;
		while (s[i][j])
		{
			if (s[i][j] == 'C')
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
